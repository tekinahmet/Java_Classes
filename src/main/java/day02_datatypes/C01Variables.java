package day02_datatypes;
public class C01Variables {
    public static void main(String[] args) {

        byte stdAge = 13;
        System.out.println("stdAge = " + stdAge);

        short numOfStdInHighSchool = 3245;
        System.out.println("numOfStdInHighSchool = " + numOfStdInHighSchool);

        int popOfKutahya = 250000;
        System.out.println("popOfKutahya = " + popOfKutahya);

        long popOfTheWorld = 7000000000L;
        System.out.println("popOfTheWorld = " + popOfTheWorld);

        float myNum = 23.99F;
        System.out.println("myNum = " + myNum);

        double weightOfHumanCell = 0.00000012;
        System.out.println("weightOfHumanCell = " + weightOfHumanCell);

        boolean isRetired = true;
        System.out.println("isRetired = " + isRetired);

        char initial = 'A';
        System.out.println("initial = " + initial);

        String stdName = "Ahmet Tekin";
        System.out.println("stdName = " + stdName);
    }
}
